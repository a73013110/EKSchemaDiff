using System.Text;

namespace ConsoleKit.Tui;

/// <summary>
/// 自有的粗塊大字字型（5 列高，A–Z／0–9／空白／連字號）。用實心方塊 █ 構字，
/// 渲染成多列純文字供 Banner 逐列套金屬漸層。中性、零外部資產，可複用於任何字標。
/// 缺字以空白格降階，不丟例外。各字形列寬不必對齊——渲染時自動補白。
/// </summary>
public static class BlockFont
{
    /// <summary>字型高度（列數）。</summary>
    public const int Height = 5;

    private const char F = '█';   // 實心
    private const char E = ' ';   // 空

    // 每個字形 5 列；以 '#' 標實心、'.' 標空，便於閱讀與對齊，渲染前轉成 █／空白。
    private static readonly Dictionary<char, string[]> Glyphs = new()
    {
        ['A'] = new[] { ".####.", "##..##", "######", "##..##", "##..##" },
        ['B'] = new[] { "##### ", "##..##", "##### ", "##..##", "##### " },
        ['C'] = new[] { ".#####", "##....", "##....", "##....", ".#####" },
        ['D'] = new[] { "##### ", "##..##", "##..##", "##..##", "##### " },
        ['E'] = new[] { "######", "##....", "##### ", "##....", "######" },
        ['F'] = new[] { "######", "##....", "##### ", "##....", "##...." },
        ['G'] = new[] { ".#####", "##....", "##.###", "##..##", ".####." },
        ['H'] = new[] { "##..##", "##..##", "######", "##..##", "##..##" },
        ['I'] = new[] { "######", "..##..", "..##..", "..##..", "######" },
        ['J'] = new[] { "######", "...##.", "...##.", "##.##.", ".###.." },
        ['K'] = new[] { "##..##", "##.##.", "####..", "##.##.", "##..##" },
        ['L'] = new[] { "##....", "##....", "##....", "##....", "######" },
        ['M'] = new[] { "##...##", "###.###", "##.#.##", "##...##", "##...##" },
        ['N'] = new[] { "##...##", "###..##", "##.#.##", "##..###", "##...##" },
        ['O'] = new[] { ".####.", "##..##", "##..##", "##..##", ".####." },
        ['P'] = new[] { "##### ", "##..##", "##### ", "##....", "##...." },
        ['Q'] = new[] { ".####.", "##..##", "##..##", "##.##.", ".##.##" },
        ['R'] = new[] { "##### ", "##..##", "##### ", "##.##.", "##..##" },
        ['S'] = new[] { ".#####", "##....", ".####.", "....##", "##### " },
        ['T'] = new[] { "######", "..##..", "..##..", "..##..", "..##.." },
        ['U'] = new[] { "##..##", "##..##", "##..##", "##..##", ".####." },
        ['V'] = new[] { "##..##", "##..##", "##..##", ".####.", "..##.." },
        ['W'] = new[] { "##...##", "##...##", "##.#.##", "###.###", "##...##" },
        ['X'] = new[] { "##..##", ".####.", "..##..", ".####.", "##..##" },
        ['Y'] = new[] { "##..##", ".####.", "..##..", "..##..", "..##.." },
        ['Z'] = new[] { "######", "...##.", "..##..", ".##...", "######" },
        ['0'] = new[] { ".####.", "##..##", "##..##", "##..##", ".####." },
        ['1'] = new[] { "..##..", ".###..", "..##..", "..##..", "######" },
        ['2'] = new[] { "##### ", "....##", ".####.", "##....", "######" },
        ['3'] = new[] { "##### ", "....##", ".####.", "....##", "##### " },
        ['4'] = new[] { "##..##", "##..##", "######", "....##", "....##" },
        ['5'] = new[] { "######", "##....", "##### ", "....##", "##### " },
        ['6'] = new[] { ".#####", "##....", "##### ", "##..##", ".####." },
        ['7'] = new[] { "######", "...##.", "..##..", ".##...", ".##..." },
        ['8'] = new[] { ".####.", "##..##", ".####.", "##..##", ".####." },
        ['9'] = new[] { ".####.", "##..##", ".#####", "....##", "##### " },
        ['-'] = new[] { "......", "......", "######", "......", "......" },
        [' '] = new[] { "....", "....", "....", "....", "...." },
    };

    /// <summary>
    /// 把字串渲染成 <see cref="Height"/> 列的粗塊大字；字元間以 <paramref name="gap"/> 個空白分隔。
    /// 大小寫不分（一律取大寫字形）；未收錄字元以一格空白降階。
    /// </summary>
    public static List<string> Render(string text, int gap = 2)
    {
        var rows = new StringBuilder[Height];
        for (int r = 0; r < Height; r++) rows[r] = new StringBuilder();

        var spacer = new string(' ', Math.Max(0, gap));
        bool first = true;
        foreach (var raw in text)
        {
            var ch = char.ToUpperInvariant(raw);
            if (!Glyphs.TryGetValue(ch, out var glyph))
                glyph = Glyphs[' '];

            int width = glyph.Max(line => line.Length);
            if (!first)
                for (int r = 0; r < Height; r++) rows[r].Append(spacer);
            first = false;

            for (int r = 0; r < Height; r++)
            {
                var line = glyph[r].PadRight(width);
                foreach (var c in line) rows[r].Append(c == '#' ? F : E);
            }
        }

        return rows.Select(sb => sb.ToString()).ToList();
    }
}
